package com.frame.core.constant;

public enum ChannelType {

	GoodsChannel("01", "商品栏目"), ContentChannel("02", "内容栏目"), AdChannel("03", "广告位"), ;
	private String code;
	private String title;

	private ChannelType(String code, String title) {
		this.code = code;
		this.title = title;
	}

	public String getCode() {
		return code;
	}

	public String getTitle() {
		return title;
	}
}
