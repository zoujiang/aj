package com.frame.core.constant;

public enum ChannelContentsStatus {
	ENABLE("01", "已启用"),
	DISABLE("02", "未启用");
	
	private String code;
	private String title;
	private ChannelContentsStatus(String code, String title) {
		this.code = code;
		this.title = title;
	}
	
	public String getTitle() {
		return this.title;
	}
	
	public String getCode() {
		return this.code;
	}
}
